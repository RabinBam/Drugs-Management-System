private JPanel createProgressPanel() {
    JPanel panel = createGlassPanel();
    panel.setLayout(new BorderLayout());
    panel.setBorder(BorderFactory.createTitledBorder("Progress Bar"));
    
    JPanel chart = new JPanel() {
        @Override
        protected void paintComponent(Graphics g) {
            Graphics2D g2 = (Graphics2D) g.create();
            g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
            
            int w = getWidth();
            int h = getHeight();
            
            // Draw background grid for readability
            g2.setColor(new Color(255, 255, 255, 20));
            for(int i = 1; i <= 4; i++) {
                int lineY = h - (i * h / 5);
                g2.drawLine(40, lineY, w - 20, lineY);
            }

            // Draw the Cyan Line Graph
            g2.setColor(new Color(0, 255, 255)); 
            g2.setStroke(new BasicStroke(2.5f));
            int[] x = {40, w/4, w/2, 3*w/4, w-30};
            int[] y = {h-50, h-90, h-70, h-120, h-80};
            
            for(int i=0; i < x.length - 1; i++) {
                g2.drawLine(x[i], y[i], x[i+1], y[i+1]);
                g2.fillOval(x[i]-3, y[i]-3, 6, 6);
            }
            g2.fillOval(x[x.length-1]-3, y[y.length-1]-3, 6, 6);
            g2.dispose();
        }
    };
    chart.setOpaque(false);
    panel.add(chart, BorderLayout.CENTER);
    return panel;
}

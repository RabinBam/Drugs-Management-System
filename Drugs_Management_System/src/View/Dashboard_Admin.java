private JPanel createCalendarPanel() {
    JPanel panel = createGlassPanel();
    panel.setLayout(new BorderLayout());
    
    // 1. Get Current Month and Year for the title
    java.util.Calendar cal = new java.util.GregorianCalendar();
    String monthName = new java.text.SimpleDateFormat("MMMM YYYY").format(cal.getTime());
    panel.setBorder(BorderFactory.createTitledBorder(null, monthName, 
            javax.swing.border.TitledBorder.CENTER, javax.swing.border.TitledBorder.TOP));

    // 2. Setup Columns and Date Logic
    String[] columns = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"};
    Object[][] days = new Object[6][7]; // 6 rows to handle all month layouts
    
    cal.set(java.util.Calendar.DAY_OF_MONTH, 1);
    int startDay = cal.get(java.util.Calendar.DAY_OF_WEEK) - 1;
    int maxDay = cal.getActualMaximum(java.util.Calendar.DAY_OF_MONTH);

    for (int day = 1, row = 0, col = startDay; day <= maxDay; day++) {
        days[row][col] = day;
        col++;
        if (col == 7) {
            col = 0;
            row++;
        }
    }

    // 3. Create and Style the JTable
    JTable calendarTable = new JTable(days, columns);
    calendarTable.setBackground(new Color(30, 50, 60));
    calendarTable.setForeground(Color.WHITE);
    calendarTable.setRowHeight(35);
    calendarTable.setGridColor(new Color(0, 255, 255, 30));
    calendarTable.setShowGrid(true);
    calendarTable.setEnabled(false); // Make it read-only
    calendarTable.setFont(new Font("SansSerif", Font.PLAIN, 14));

    // 4. Center the Text in Cells
    javax.swing.table.DefaultTableCellRenderer centerRenderer = new javax.swing.table.DefaultTableCellRenderer();
    centerRenderer.setHorizontalAlignment(JLabel.CENTER);
    centerRenderer.setOpaque(false); // Maintain glass look
    for (int i = 0; i < calendarTable.getColumnCount(); i++) {
        calendarTable.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
    }

    // 5. Style the Table Header (Sun, Mon, etc.)
    calendarTable.getTableHeader().setBackground(new Color(20, 40, 50));
    calendarTable.getTableHeader().setForeground(Color.CYAN);
    calendarTable.getTableHeader().setFont(new Font("SansSerif", Font.BOLD, 12));
    calendarTable.getTableHeader().setBorder(BorderFactory.createLineBorder(new Color(0, 255, 255, 50)));

    // 6. Final Layout Assembly
    JScrollPane scroll = new JScrollPane(calendarTable);
    scroll.setOpaque(false);
    scroll.getViewport().setOpaque(false);
    scroll.setBorder(BorderFactory.createEmptyBorder(5, 5, 5, 5)); // Add padding
    
    panel.add(calendarTable.getTableHeader(), BorderLayout.NORTH); // Manually add header for cleaner look
    panel.add(scroll, BorderLayout.CENTER);
    
    return panel;
}
private ImageIcon loadAndScaleImage(String path, int maxWidth, int maxHeight) {
    java.net.URL imgUrl = getClass().getResource(path);
    if (imgUrl == null) {
        System.err.println("Resource not found: " + path);
        return null;
    }
    
    ImageIcon icon = new ImageIcon(imgUrl);
    Image img = icon.getImage();

    int w = img.getWidth(null);
    int h = img.getHeight(null);

    if (w <= 0 || h <= 0) {
        System.err.println("Invalid image dimensions.");
        return icon;
    }

    double ratio = Math.min((double) maxWidth / w, (double) maxHeight / h);
    int newW = (int) (w * ratio);
    int newH = (int) (h * ratio);

    Image scaledImg = img.getScaledInstance(newW, newH, Image.SCALE_SMOOTH);
    return new ImageIcon(scaledImg);
}


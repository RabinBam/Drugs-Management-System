public void updateDashboardStats() {
    double totalEarning = 0;
    int completedCount = 0;
    java.util.Map<String, Integer> salesCounts = new java.util.HashMap<>();
    java.util.Map<String, Model.Drug> drugMetadata = new java.util.HashMap<>();

    if (historyModel != null && historyModel.getHistoryStack() != null) {
        for (Model.Drug d : historyModel.getHistoryStack().getAll()) {
            totalEarning += d.getUnitCost();
            completedCount++;
            salesCounts.put(d.getName(), salesCounts.getOrDefault(d.getName(), 0) + 1);
            drugMetadata.putIfAbsent(d.getName(), d);
        }
    }

    // --- FONT COLOR: WHITE ---
    if (lblSalesValue != null) {
        lblSalesValue.setText(completedCount + " pcs");
        lblSalesValue.setForeground(Color.WHITE); 
    }
    if (lblEarningValue != null) {
        lblEarningValue.setText("RS. " + String.format("%.2f", totalEarning));
        lblEarningValue.setForeground(Color.WHITE);
    }

    // Update Inventory Table
    if (topSalesTable != null) {
        DefaultTableModel model = (DefaultTableModel) topSalesTable.getModel();
        model.setRowCount(0);
        salesCounts.entrySet().stream()
            .sorted(java.util.Map.Entry.<String, Integer>comparingByValue().reversed())
            .forEach(entry -> {
                Model.Drug d = drugMetadata.get(entry.getKey());
                model.addRow(new Object[]{
                    entry.getKey(), d.getVendor(), "RS. " + d.getUnitCost(), entry.getValue()
                });
            });
    }

    // --- PIE CHART LEGEND FINE-TUNING ---
    if (pieChartComponent != null) {
        int pending = (salesModel != null) ? salesModel.getSalesCount() : 0;
        
        // Creating dynamic labels for the legend
        String[] labelsWithValues = {
            "Done: " + completedCount, 
            "Queue: " + pending
        };
        
        pieChartComponent.setData(
            new double[]{(double)completedCount, (double)pending},
            labelsWithValues,
            new Color[]{new Color(0, 255, 213), new Color(255, 80, 80)}
        );
    }
}

private void styleTable(JTable table) {
    table.setBackground(new Color(25, 35, 45));
    table.setForeground(Color.WHITE);
    table.setRowHeight(30);
    table.setFont(new Font("Segoe UI", Font.PLAIN, 14));
    table.setGridColor(new Color(0, 255, 213, 50));
    table.getTableHeader().setBackground(new Color(15, 20, 25));
    table.getTableHeader().setForeground(new Color(180, 180, 180)); 
    table.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 12));
    
    table.setSelectionBackground(new Color(0, 255, 213, 40));
}
